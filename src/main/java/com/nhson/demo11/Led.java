package com.nhson.demo11;

import java.util.Date;

public class Led {
    private boolean isOn;
    private Date date;

    public Led(boolean isOn, Date date) {
        this.isOn = isOn;
        this.date = date;
    }

    public void setOn(boolean on) {
        isOn = on;
    }

    public void setDate(Date date) {
        this.date = date;
    }

    public boolean isOn() {
        return isOn;
    }

    public Date getDate() {
        return date;
    }

    public String toString(){
        return "" +
                "{" +
                "isOn=" + isOn +
                ", date=" + date +
                "}";
    }

}
